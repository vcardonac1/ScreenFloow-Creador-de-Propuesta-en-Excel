def fill_screens_maps(ws: Worksheet, report: List[str], missing_info: List[str]):
    headers = rescan_headers(ws, SCREENS_HEADER_ROW)
    required = ["Latitude", "Longitude", "Google Maps"]
    miss = [h for h in required if h not in headers]
    if miss:
        missing_info.append(f"Screens: missing columns for fill {miss}")
        return

    lat_col = headers["Latitude"]
    lng_col = headers["Longitude"]
    maps_col = headers["Google Maps"]

    start_row = SCREENS_HEADER_ROW + 1

    for row in range(start_row, ws.max_row + 1):
        lat = clean_number(ws.cell(row=row, column=lat_col).value)
        lng = clean_number(ws.cell(row=row, column=lng_col).value)

        maps_url = f"https://www.google.com/maps?q={lat},{lng}" if (lat is not None and lng is not None) else ""
        ws.cell(row=row, column=maps_col).value = maps_url

    report.append(f"Screens: filled Google Maps URLs.")


