            # Determinamos hasta qué columna extender el formato
            max_col = ws.max_column

            # 3. Iterar por las filas 1 a la 6
            for r in range(1, 7):
                # La celda origen es siempre la de la columna A (columna 1)
                celda_origen = ws.cell(row=r, column=1)

                # 4. Copiar formato a todas las demás columnas en esa fila
                for c in range(2, max_col + 1):
                    celda_destino = ws.cell(row=r, column=c)

                    # Copiamos los atributos de estilo
                    if celda_origen.has_style:
                        celda_destino.font = copy(celda_origen.font)
