def clear_block_from_row(summary_ws: Worksheet, start_row: int):
    stop_row = summary_ws.max_row
    for rr in range(start_row + 1, summary_ws.max_row + 1):
        if norm(summary_ws.cell(row=rr, column=2).value) != "":
            stop_row = rr - 1
            break
    for rr in range(start_row, stop_row + 1):
        summary_ws.cell(row=rr, column=3).value = None


