def copy_campaign_sheet_values_only(src_vals_ws: Worksheet, src_style_ws: Worksheet, dst_wb: Workbook, dst_title: str, report: List[str]) -> Worksheet:
    if dst_title in dst_wb.sheetnames:
        del dst_wb[dst_title]
    dst_ws = dst_wb.create_sheet(dst_title)

    _copy_sheet_level_settings(src_style_ws, dst_ws)

    for col_letter, dim in src_style_ws.column_dimensions.items():
        dst_ws.column_dimensions[col_letter].width = dim.width
    for row_idx, dim in src_style_ws.row_dimensions.items():
        dst_ws.row_dimensions[row_idx].height = dim.height

    for merged in src_style_ws.merged_cells.ranges:
        dst_ws.merge_cells(str(merged))

    for r in range(1, src_style_ws.max_row + 1):
        for c in range(1, src_style_ws.max_column + 1):
            sc = src_style_ws.cell(row=r, column=c)
            vc = src_vals_ws.cell(row=r, column=c)
            dc = dst_ws.cell(row=r, column=c)
            dc.value = vc.value
            if sc.has_style:
                dc._style = copy(sc._style)
            dc.number_format = sc.number_format
            dc.protection = copy(sc.protection)
            dc.alignment = copy(sc.alignment)
            dc.font = copy(sc.font)
            dc.fill = copy(sc.fill)
            dc.border = copy(sc.border)
            dc.comment = sc.comment

    try:
        dst_ws.tables.clear()
    except Exception:
        pass

    report.append(f"Copied sheet '{dst_title}' values-only into template.")
    return dst_ws

