def extract_unique_column_values_from_sheet(ws: Worksheet, column_name: str) -> List[str]:
    header_row = detect_header_row(ws, {column_name}, max_scan_rows=10, default_row=SCREENS_HEADER_ROW)
    col_idx = None
    for c in range(1, ws.max_column + 1):
        if norm(ws.cell(row=header_row, column=c).value) == column_name:
            col_idx = c
            break
    if col_idx is None:
        return []

    s = set()
    out = []
    for r in range(header_row + 1, ws.max_row + 1):
        v = norm(ws.cell(row=r, column=col_idx).value)
        if v and v not in s:
            s.add(v)
            out.append(v)
    out.sort(key=lambda x: x.casefold())
    return out


def country_value_from_screens(ws: Worksheet) -> Optional[str]:
    countries = extract_unique_column_values_from_sheet(ws, "Country")
    if not countries:
        return None
    if len(countries) == 1:
        return countries[0]
    return f"Multi-Country [{', '.join(countries)}]"

