def merge_creative_specs_to_campaign():
    # 1. Localizar los archivos usando patrones glob
    file_specs_list = glob.glob("Creative specs for campaign*MID.xlsx")
    file_campaign_list = glob.glob("Campaign - *MID.xlsx")

    if not file_specs_list or not file_campaign_list:
        print("No se encontraron los archivos necesarios.")
        if not file_specs_list: print("- Falta: Creative specs...MID.xlsx")
        if not file_campaign_list: print("- Falta: Campaign...MID.xlsx")
        return

    # Tomamos el archivo de campaña (asumimos que es uno solo el destino)
    path_campaign = file_campaign_list[0]
    print(f"Leyendo Campaña base de: {path_campaign}")
    wb_campaign = load_workbook(path_campaign)

    # 2. Iterar sobre cada archivo de Specs encontrado (pueden ser 1 o 2)
    for path_specs in file_specs_list:
        print(f"Procesando Specs de: {path_specs}")
        wb_specs = load_workbook(path_specs)

        if "Required creatives" not in wb_specs.sheetnames:
            print(f"Error: La hoja 'Required creatives' no existe en {path_specs}.")
            continue

        ws_source = wb_specs["Required creatives"]

        # --- Lógica de Renombrado Dinámico ---
        # Leemos la fila 2 para buscar "Length"
        header_row = 2
        tiene_length = False
        for c in range(1, ws_source.max_column + 1):
            if str(ws_source.cell(row=header_row, column=c).value).strip() == "Length":
                tiene_length = True
                break

        nuevo_nombre_hoja = "Required creatives de Video" if tiene_length else "Required creatives de Imagen"

        # Evitar errores si la hoja ya existe por un proceso previo o archivo duplicado
        if nuevo_nombre_hoja in wb_campaign.sheetnames:
            del wb_campaign[nuevo_nombre_hoja]

        ws_target = wb_campaign.create_sheet(title=nuevo_nombre_hoja)

        # 3. Copiar celdas con formato, estilos y valores (Tu lógica original)
        print(f"Copiando contenido a '{nuevo_nombre_hoja}'...")
        for row in ws_source.iter_rows():
            for cell in row:
                new_cell = ws_target.cell(row=cell.row, column=cell.column, value=cell.value)
                if cell.has_style:
                    new_cell.font = copy(cell.font)
                    new_cell.border = copy(cell.border)
                    new_cell.fill = copy(cell.fill)
                    new_cell.number_format = copy(cell.number_format)
                    new_cell.protection = copy(cell.protection)
                    new_cell.alignment = copy(cell.alignment)

        # Copiar anchos de columna
        for col_name, col_dim in ws_source.column_dimensions.items():
            ws_target.column_dimensions[col_name].width = col_dim.width

        # Copiar altos de fila
        for row_idx, row_dim in ws_source.row_dimensions.items():
            ws_target.row_dimensions[row_idx].height = row_dim.height

        # 4. Reordenar la hoja para que vaya justo después de "Line items"
        sheet_names = wb_campaign.sheetnames
        if "Line items" in sheet_names:
            idx_line_items = sheet_names.index("Line items")
            current_idx = sheet_names.index(nuevo_nombre_hoja)
            sheet_to_move = wb_campaign._sheets.pop(current_idx)
            # Insertamos después de Line items (o después de la anterior Required Creatives insertada)
            wb_campaign._sheets.insert(idx_line_items + 1, sheet_to_move)

    # 5. Guardar el resultado final
    new_filename = path_campaign.replace("MID.xlsx", "UPDATED.xlsx")
    try:
        wb_campaign.save(new_filename)
        print(f"Éxito: Archivo final guardado como {new_filename}")
    except PermissionError:
        print("Error: No se pudo guardar el archivo. Asegúrate de que no esté abierto.")


def limpiar_directorio_mid():
    # 1. Definir los patrones de búsqueda
    patron_specs_mid = "Creative specs for campaign*MID.xlsx"
    patron_campaign_mid = "Campaign - *MID.xlsx"

    # 2. Lógica previa: Si NO hay archivos de Specs MID
    specs_mid_existentes = glob.glob(patron_specs_mid)

    if not specs_mid_existentes:
        print("No se encontraron archivos 'Creative specs...MID.xlsx'.")
        # Buscamos los archivos de Campaign MID para salvarlos como UPDATED
        campaigns_mid = glob.glob(patron_campaign_mid)

        for f_mid in campaigns_mid:
            # Definimos el nombre del UPDATED reemplazando MID
            nombre_updated = f_mid.replace("MID.xlsx", "UPDATED.xlsx")
            try:
                # Copiamos y guardamos el archivo con el nuevo nombre
                shutil.copy(f_mid, nombre_updated)
                print(f"  - Guardado: {nombre_updated} (copia de {f_mid})")
            except Exception as e:
                print(f"  - Error al intentar guardar {nombre_updated}: {e}")

    # 3. Recopilar todos los archivos MID que coinciden para borrar
    patrones_limpieza = [patron_specs_mid, patron_campaign_mid]
    archivos_encontrados = []

    for patron in patrones_limpieza:
        archivos_encontrados.extend(glob.glob(patron))

    if not archivos_encontrados:
        print("No se encontraron archivos MID para eliminar.")
        return

    print(f"\nSe han encontrado {len(archivos_encontrados)} archivos para eliminar:")

    # 4. Proceder al borrado de los archivos temporales (MID)
    for archivo in archivos_encontrados:
        try:
            os.remove(archivo)
            print(f"  - Eliminado con éxito: {archivo}")
        except OSError as e:
            print(f"  - Error al eliminar {archivo}: {e.strerror}")

    print("\nLimpieza completada.")

