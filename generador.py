    if "Longitude" not in headers:
        report.append("Screens: missing 'Longitude' column; skipped Google Maps.")
        return

    lng_idx = headers["Longitude"]
    maps_pos = lng_idx + 1

    if "Google Maps" not in headers:
        ws.insert_cols(maps_pos)
