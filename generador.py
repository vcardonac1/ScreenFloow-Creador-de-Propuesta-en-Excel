def procesar_sov_mid():
    # 1. Buscar el archivo que termina en MID.xlsx
    archivos = glob.glob("*MID.xlsx")
    if not archivos:
        print("No se encontró ningún archivo que termine en 'MID.xlsx'")
        return

    file_path = archivos[0]
    print(f"Procesando: {file_path}")

    # Cargamos con data_only=True para obtener los valores numéricos de las celdas
    wb = load_workbook(file_path, data_only=True)

    if "Line items" not in wb.sheetnames or "Summary" not in wb.sheetnames:
        print("Faltan hojas necesarias en el archivo.")
        return

    # --- PASO 1: Calcular sumas en "Line items" ---
    ws_li = wb["Line items"]
    header_row = 2  # Ajustar si el encabezado está en otra fila

    # Mapear columnas por nombre
    headers = {ws_li.cell(row=header_row, column=c).value: c for c in range(1, ws_li.max_column + 1)}
    col_est = headers.get("Est. Impressions")
    col_max = headers.get("Max. Impressions")
    col_item = headers.get("Line item")

    est_impr = 0
    max_impr = 0

    # Iterar filas saltando el header y excluyendo la fila "Total"
    for r in range(header_row + 1, ws_li.max_row + 1):
        # Si la celda en la columna 'Line item' dice 'Total', dejamos de sumar
        item_name = str(ws_li.cell(row=r, column=col_item).value).strip().lower() if col_item else ""
        if "total" in item_name:
            continue

        val_est = ws_li.cell(row=r, column=col_est).value
        val_max = ws_li.cell(row=r, column=col_max).value

        if isinstance(val_est, (int, float)): est_impr += val_est
        if isinstance(val_max, (int, float)): max_impr += val_max

    wb.close() # Cerramos la versión de solo datos

    # --- PASO 2: Escribir en "Summary" con formato ---
    # Cargamos el archivo normal para guardar cambios y estilos
    wb_final = load_workbook(file_path)
    ws_sum = wb_final["Summary"]

    # Verificar si SOV ya existe en columna B
    sov_existe = False
    for r in range(1, ws_sum.max_row + 1):
        if str(ws_sum.cell(row=r, column=2).value).strip().upper() == "SOV":
            sov_existe = True
            break

    if not sov_existe:
        # Buscar "Playouts" para insertar debajo
        row_playouts = None
        for r in range(1, ws_sum.max_row + 1):
            if str(ws_sum.cell(row=r, column=2).value).strip() == "Playouts":
                row_playouts = r
                break

        if row_playouts:
            target_row = row_playouts + 1
            ws_sum.insert_rows(target_row)

            # Definir estilo de fuente (Tamaño 14)
            fuente_14 = Font(size=14)

            # Escribir "SOV" en columna B
            cell_b = ws_sum.cell(row=target_row, column=2)
            cell_b.value = "SOV"
            cell_b.font = fuente_14

            # Calcular y escribir valor en columna C
            cell_c = ws_sum.cell(row=target_row, column=3)
            cell_c.value = est_impr / max_impr if max_impr > 0 else 0
            cell_c.number_format = '0.00%' # Formato porcentaje
            cell_c.font = fuente_14

            print(f"Fila SOV añadida con valor: {cell_c.value:.2%}")
        else:
            print("No se encontró la celda 'Playouts' en la hoja Summary.")
    else:
        print("La celda SOV ya existe en la hoja Summary.")

    # Guardar el archivo
    wb_final.save(file_path)
    print("Archivo MID actualizado exitosamente.")

