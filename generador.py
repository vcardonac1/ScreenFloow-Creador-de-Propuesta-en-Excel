def norm(v: Any) -> str:
    return "" if v is None else str(v).strip()

def norm_lower(v: Any) -> str:
    return norm(v).lower()

def is_formula_cell(cell) -> bool:
    return getattr(cell, "data_type", None) == "f" or (isinstance(cell.value, str) and str(cell.value).startswith("="))

def clean_number(value):
    if value is None:
        return None
    if isinstance(value, str):
        value = value.replace(",", ".")
    try:
        return float(value)
    except Exception:
        return None

def safe_to_float(v) -> Optional[float]:
    return clean_number(v)

def parse_excel_date(v) -> Optional[datetime]:
    if v is None:
        return None
    if isinstance(v, datetime):
        return v
    if isinstance(v, date):
        return datetime(v.year, v.month, v.day)
    s = str(v).strip()
    if not s:
        return None
    for fmt in (
        "%m/%d/%Y %H:%M:%S",
        "%m/%d/%Y",
        "%Y-%m-%d",
        "%d/%m/%Y",
        "%d/%m/%Y %H:%M:%S",
    ):
        try:
            return datetime.strptime(s, fmt)
        except Exception:
            continue
    return None

def fmt_date(dt: Optional[datetime]) -> str:
    return dt.strftime("%Y-%m-%d") if dt else ""


def detect_header_row(ws: Worksheet, expected_headers: Set[str], max_scan_rows: int = 10, default_row: int = DEFAULT_HEADER_ROW_NON_TEMPLATE) -> int:
    for r in range(1, max_scan_rows + 1):
        row_vals = [norm(ws.cell(row=r, column=c).value) for c in range(1, min(ws.max_column, 150) + 1)]
        if any(v in expected_headers for v in row_vals):
            return r
    return default_row


def find_cell_by_exact_text(ws: Worksheet, text: str, search_cols: int = 12, search_rows: int = 800) -> Optional[Tuple[int, int]]:
    t = text.strip().lower()
    max_r = min(search_rows, ws.max_row)
    max_c = min(search_cols, ws.max_column)
    for r in range(1, max_r + 1):
        for c in range(1, max_c + 1):
            if norm_lower(ws.cell(row=r, column=c).value) == t:
                return (r, c)
    return None


def get_value_right_or_down(ws: Worksheet, label: str, prefer_right: bool = True) -> Any:
    pos = find_cell_by_exact_text(ws, label)
    if not pos:
        return None
    r, c = pos
    if prefer_right:
        vr = ws.cell(row=r, column=c + 1).value if c + 1 <= ws.max_column else None
        if norm(vr) != "":
            return vr
        vd = ws.cell(row=r + 1, column=c).value if r + 1 <= ws.max_row else None
        if norm(vd) != "":
            return vd
    else:
        vd = ws.cell(row=r + 1, column=c).value if r + 1 <= ws.max_row else None
        if norm(vd) != "":
            return vd
        vr = ws.cell(row=r, column=c + 1).value if c + 1 <= ws.max_column else None
        if norm(vr) != "":
            return vr
    return None


