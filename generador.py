def ensure_line_items_total_row(ws: Worksheet, report: List[str], missing_info: List[str]):
    header_row = DEFAULT_HEADER_ROW_NON_TEMPLATE
    headers = {}
    for c in range(1, ws.max_column + 1):
        h = norm(ws.cell(row=header_row, column=c).value)
        if h:
            headers[h] = c

    line_item_col = headers.get("Line item", 1)
    total_row = None
    for r in range(header_row + 1, ws.max_row + 1):
        if norm_lower(ws.cell(row=r, column=line_item_col).value) == "total":
            total_row = r
            break

    if not total_row:
        total_row = ws.max_row + 1
        ws.cell(row=total_row, column=line_item_col).value = "Total"
        report.append(f"Line items: created Total row at {total_row}.")
    else:
        report.append(f"Line items: found Total row at {total_row}.")

    data_start = header_row + 1
    data_end = total_row - 1
    if data_end < data_start:
        missing_info.append("Line items: no data rows above Total")
        return

    def set_sum(col_name: str):
        col = headers.get(col_name)
        if not col:
            return
        col_letter = get_column_letter(col)
        ws.cell(row=total_row, column=col).value = f"=SUM({col_letter}{data_start}:{col_letter}{data_end})"

    # Sum totals
    for col_name in ["Screens", "Locations", "Publishers", "Total budget goal", "Max. Impressions", "Max. Spend", "Est. Impressions", "Est. Spend"]:
        if col_name in headers:
            set_sum(col_name)

    # Est. CPM total formula
    cpm_col = headers.get("Est. CPM")
    spend_col = headers.get("Est. Spend")
    imps_col = headers.get("Est. Impressions")
    if cpm_col and spend_col and imps_col:
        spend_letter = get_column_letter(spend_col)
        imps_letter = get_column_letter(imps_col)
        ws.cell(row=total_row, column=cpm_col).value = f"=SUM({spend_letter}{data_start}:{spend_letter}{data_end})/(SUM({imps_letter}{data_start}:{imps_letter}{data_end})/1000)"
        report.append("Line items: Est. CPM total formula set.")
    else:
        missing_info.append("Line items: missing Est. CPM and/or Est. Spend and/or Est. Impressions")


