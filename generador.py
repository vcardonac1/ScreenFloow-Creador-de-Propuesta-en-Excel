def get_forecast_estimated_in_realtime(ws: Worksheet) -> Tuple[Optional[float], Optional[float]]:
    start = find_cell_by_exact_text(ws, "Estimated In-Realtime", search_cols=12, search_rows=800)
    if not start:
        start = find_cell_by_exact_text(ws, "Forecast", search_cols=12, search_rows=800)
    if not start:
        return (None, None)

    start_row = start[0]
    impressions = None
    playouts = None

    for r in range(start_row, min(ws.max_row, start_row + 100) + 1):
        for c in range(1, min(ws.max_column, 12) + 1):
            v = ws.cell(row=r, column=c).value
            if norm(v) == "Impressions" and impressions is None:
                impressions = safe_to_float(ws.cell(row=r, column=c + 1).value)
            if norm(v) == "Playouts" and playouts is None:
                playouts = safe_to_float(ws.cell(row=r, column=c + 1).value)
        if impressions is not None and playouts is not None:
            break

    return (impressions, playouts)

