    archivos = glob.glob("*MID.xlsx")

    if not archivos:
        print("No se encontró ningún archivo que termine en 'MID.xlsx'")
        return

    nombre_archivo = archivos[0]
    print(f"Procesando: {nombre_archivo}")

    # 2. Cargar el libro de trabajo (sin data_only para preservar fórmulas si las hubiera)
    wb = load_workbook(nombre_archivo)
    hojas_objetivo = ["Specs de Video", "Specs de Imagen"]
