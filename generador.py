def update_hyperlink_target_and_text(ws: Worksheet, old_visible_text: str, new_visible_text: str, new_url: str, report: List[str]) -> bool:
    target = norm_lower(old_visible_text)
    for r in range(1, ws.max_row + 1):
        for c in range(1, ws.max_column + 1):
            cell = ws.cell(row=r, column=c)
            if norm_lower(cell.value) == target:
                cell.value = new_visible_text
                if cell.hyperlink:
                    cell.hyperlink.target = new_url
                else:
                    cell.hyperlink = new_url
                report.append(f"Updated Map link text to '{new_visible_text}'.")
                return True
    report.append("Interactive Map URL cell not found by visible text.")
    return False


